function esc($s){ return htmlspecialchars((string)$s, ENT_QUOTES, 'UTF-8'); }
function active_class($page_name) {
    $current_page = basename($_SERVER['PHP_SELF']);
    if ($current_page === 'manage_courses.php' || $current_page === 'edit_courses.php') {
        if ($page_name === 'manage_courses.php') echo 'class="active"';
    } elseif ($current_page === $page_name) {
        echo 'class="active"';
    }
}

