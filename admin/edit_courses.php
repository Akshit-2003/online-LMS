// --- Handle Form Submission (POST) ---
if ($_SERVER['REQUEST_METHOD'] === 'POST' && isset($_POST['course_slug'])) {
    $slug = trim($_POST['course_slug']);
    if (empty($slug)) {
        $_SESSION['message'] = ['type' => 'error', 'text' => 'Course Slug cannot be empty.'];
    } else {
        // Sanitize slug: lowercase, alphanumeric, and dashes
        $slug = strtolower(preg_replace('/[^a-zA-Z0-9-]+/', '', str_replace(' ', '-', $slug)));

        $is_new = !isset($all_courses[$slug]);
        $course_data = $all_courses[$slug] ?? [
            'title' => '', 'short' => '', 'description' => '', 'thumb' => '../images/course_placeholder.jpg',
            'instructor' => '', 'level' => 'Beginner', 'duration' => '', 'language' => 'English', 'intro_video_id' => '',
            'prerequisites' => [], 'category' => '', 'tags' => [], 'syllabus' => [], 'resources' => [], 'related' => []
        ];

        // Update fields from POST
        $course_data['title'] = trim($_POST['title'] ?? '');
        $course_data['short'] = trim($_POST['short'] ?? '');
        $course_data['thumb'] = trim($_POST['current_thumb'] ?? $course_data['thumb']); // Keep old thumb by default

        // --- Handle Thumbnail Upload ---
        if (isset($_FILES['thumb_image']) && $_FILES['thumb_image']['error'] === UPLOAD_ERR_OK) {
            $thumb_file = $_FILES['thumb_image'];
            $target_dir = __DIR__ . "/../uploads/thumbnails/";
            if (!is_dir($target_dir)) {
                mkdir($target_dir, 0755, true);
            }

            $image_info = getimagesize($thumb_file["tmp_name"]);
            $image_ext = strtolower(pathinfo($thumb_file["name"], PATHINFO_EXTENSION));
            $allowed_exts = ['jpg', 'jpeg', 'png', 'gif'];

            if ($image_info === false) {
                $_SESSION['message'] = ['type' => 'error', 'text' => 'Uploaded file is not a valid image.'];
            } elseif ($thumb_file["size"] > 2000000) { // 2MB limit
                $_SESSION['message'] = ['type' => 'error', 'text' => 'Image file is too large (max 2MB).'];
            } elseif (!in_array($image_ext, $allowed_exts)) {
                $_SESSION['message'] = ['type' => 'error', 'text' => 'Only JPG, JPEG, PNG, and GIF files are allowed.'];
            } else {
                $new_thumb_name = $slug . '-' . time() . '.' . $image_ext;
                $target_file = $target_dir . $new_thumb_name;
                if (move_uploaded_file($thumb_file["tmp_name"], $target_file)) {
                    // Path should be relative to the client folder where it's used
                    $course_data['thumb'] = '../uploads/thumbnails/' . $new_thumb_name;
                }
            }
        }

        $course_data['description'] = trim($_POST['description'] ?? '');
        $course_data['instructor'] = trim($_POST['instructor'] ?? '');
        $course_data['intro_video_id'] = trim($_POST['intro_video_id'] ?? '');
        $course_data['level'] = trim($_POST['level'] ?? 'Beginner');
        $course_data['duration'] = trim($_POST['duration'] ?? '');
        $course_data['category'] = trim($_POST['category'] ?? '');

        $all_courses[$slug] = $course_data;

        // Save back to file
        $output = "<?php\n\n// This file is auto-generated by the admin editor. Do not modify manually.\n\nreturn " . var_export($all_courses, true) . ";\n";
        if (file_put_contents($study_content_file, $output, LOCK_EX)) {
            $_SESSION['message'] = ['type' => 'success', 'text' => 'Course ' . ($is_new ? 'created' : 'updated') . ' successfully!'];
        } else {
            $_SESSION['message'] = ['type' => 'error', 'text' => 'Error saving course data. Check file permissions.'];
        }
    }
    header("Location: edit_courses.php");
    exit();
}

